    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
       
        jTextFieldPesquisar.setText("");
        if (jTextFieldNomeDoMaterial.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha O Campo 'Nome Do Material' Para Continuar!");
           jTextFieldNomeDoMaterial.requestFocus();
       }else if(jTextFieldQuantidade.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha O Campo 'Quantidade' Para Continuar!");
           jTextFieldQuantidade.requestFocus();
       }else if(jTextFieldValorPorUnidade.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha O Campo 'Valor Por Unidade' Para Continuar!");
           jTextFieldValorPorUnidade.requestFocus();
       /*}else if(jTextFieldDataDeRegistro.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha O Campo 'Data De Registro' Para Continuar!");
           jTextFieldDataDeRegistro.requestFocus();*/
       } else if (flag==1){ 

        mod.setNome(jTextFieldNomeDoMaterial.getText());
        mod.setQuantidade(Integer.valueOf(jTextFieldQuantidade.getText()));
        mod.setValor(Float.valueOf(jTextFieldValorPorUnidade.getText()));
        control.salvar(mod);  
            
        jButtonEditar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);
        
        jTextFieldNomeDoMaterial.setEnabled(false);
        jTextFieldValorPorUnidade.setEnabled(false);
        jTextFieldQuantidade.setEnabled(false);

        jTextFieldNomeDoMaterial.setText("");
        jTextFieldQuantidade.setText("");
        jTextFieldValorPorUnidade.setText("");
        jTextFieldId.setText("");

        preencherTabela("select * from material order by material_id");
        
       } else{
         
        jTextFieldNomeDoMaterial.setEnabled(false);
        jTextFieldValorPorUnidade.setEnabled(false);
        jTextFieldQuantidade.setEnabled(false);           
           
        jButtonEditar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);            
        
        mod.setCodigo(Integer.parseInt(jTextFieldId.getText()));
        mod.setNome(jTextFieldNomeDoMaterial.getText());
        mod.setQuantidade(Integer.valueOf(jTextFieldQuantidade.getText()));
        mod.setValor(Float.valueOf(jTextFieldValorPorUnidade.getText()));
        control.editar(mod);
        
        jTextFieldNomeDoMaterial.setText("");
        jTextFieldQuantidade.setText("");
        jTextFieldValorPorUnidade.setText("");
        jTextFieldId.setText("");
        preencherTabela("select * from material order by material_id");
       }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

